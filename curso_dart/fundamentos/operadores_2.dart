main() {
  //Operadores de Atribuição (Binario / INFIX)
  double a = 2;
  a = a + 3;
  a += 3;
  a -= 3;
  a *= 3;
  a /= 5;
  print(a);

  //Operadores relacionais (Binario / Infix) - Resultado sempre BOOL

  print(3 > 2);
  print(3 >= 3);
  print(3 < 4);
  print(3 <= 3);
  print(3 != 3);
  print(3 == 3);
  print(3 == '3');

  print(2 + 5 > 3 - 1 && 4 + 7 != 4 - 7);
}
